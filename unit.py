def bar_to_Mpa(bar) :
    return bar * 0.1

def kelvin_to_celciuos(kelvin) :
    return kelvin - 273.15

def celciuos_to_kelvin(celciuos) :
    return celciuos + 273.15

def bar_to_pa(bar) :
    return bar * 100000

def kpa_to_pa(kpa) :
    return kpa * 1000

def bar_to_kpa(bar) :
    return bar * 100

def kpa_to_bar(kpa) :
    return kpa / 100

def pa_to_Mpa(pa) :
    return pa / 1000000

def pa_to_kpa(pa) :
    return pa / 1000

def pa_to_bar(pa) :
    return pa / 100000

